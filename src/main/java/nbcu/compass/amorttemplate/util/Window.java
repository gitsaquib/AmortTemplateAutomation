package nbcu.compass.amorttemplate.util;

public class Window {

	private String startDate;
	private String endDate;
	private String runInPlayDay;
	private String runsPDAllowed;
	private String definition;
	
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getRunInPlayDay() {
		return runInPlayDay;
	}
	public void setRunInPlayDay(String runInPlayDay) {
		this.runInPlayDay = runInPlayDay;
	}
	public String getRunsPDAllowed() {
		return runsPDAllowed;
	}
	public void setRunsPDAllowed(String runsPDAllowed) {
		this.runsPDAllowed = runsPDAllowed;
	}
	public String getDefinition() {
		return definition;
	}
	public void setDefinition(String definition) {
		this.definition = definition;
	}
}
